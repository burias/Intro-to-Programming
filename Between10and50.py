A = int(input("Enter first number. "))
B = int(input("Enter second number. "))

if A <= 50 and A >= 10:
    print("A is between 10 and 50, and its value is", A)
else:
    print("A is not between 10 and 50.")

if B <= 50 and B >= 10:
    print("B is between 10 and 50, and its value is", B)
else:
    print("B is not between 10 and 50.")